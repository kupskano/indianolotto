          Emirates Lotto has been suspended, or "temporarily paused", only months after it launched on 18th April. In an email to subscribers, a representative of Emirates Lotto indicated the pausing of the sale of their collectables was for a planned upgrade.

          However, it must be questioned why an upgrade is required on a new product so soon, especially as no date was given for the resumption of the draws. One possible reason might be a lack of sales, or entries, into the draws.  That is highly likely given the relatively complicated entry mechanism, involving the purchase of a ‘collectable’ card, scratching off a 16-digit code and using that to select your numbers.

          With low sales, also comes low numbers of winners.  The Emirates Loto have announced a few winners, like Mohammad Khalid who won AED 350,000, but there have been no jackpot winners to date and the jackpot itself has been stuck at AED 50 million for a number of weeks.

          The email went on to say “At Emirates Loto, we are always exploring ways of making your experience with more convenient and enjoyable. So, expect a more customer-centric and seamless experience when we return. While we are upgrading our experiences, we will continue to support communities across the UAE through philanthropic and CSR initiatives in line with our mission and values.”

          Unclaimed prizes can still be received by calling 800 5825 in Dubai or by email through their customer support services.

          If you have already bought collectables for future draws, you can claim a refund or carry them forward to the new platform.

